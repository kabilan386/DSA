# Data Type:
# String:
str = "hello"

print(str[0])

# int:
val = 10

print(val + 10)

# float:
floatnum = 10.5

print(floatnum + 10)

# Complex
complexnum = 10 + 20j

print(complexnum + 10)

# Boolean
boolval = True

print(boolval + 10)

# List
listval = [1,2,3,4,5]

print(listval[0])

# Tuple
tupleval = (1,2,3,4,5)

print(tupleval[0])

# Set
setval = {1,2,3,4,5}

print(setval)

# Dictionary
dictval = {"name":"John","age":30}

print(dictval["name"])

# Range
rangeval = range(10)

print(rangeval[0],rangeval)

# Bytes
bytesval = b"hello"

print(bytesval[0])

# Bytearray
bytearrayval = bytearray(b"hello")

print(bytearrayval[0])

# Memoryview
memoryviewval = memoryview(b"hello")

print(memoryviewval[0])

# None
noneval = None

print(noneval)


# Frozen Set

val = frozenset({1,2,3,4,5})

print(val)


